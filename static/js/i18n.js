// Language codes display
const langDisplay = {
    en: 'EN',
    hi: 'हिं',
    ta: 'த',
    te: 'తె',
    ml: 'മ'
};

/**
 * Get current language from localStorage
 */
function getCurrentLanguage() {
    return localStorage.getItem('jalscan_language') || 'en';
}

/**
 * Set current language and save to localStorage
 */
function setLanguage(lang) {
    localStorage.setItem('jalscan_language', lang);
    applyTranslations(lang);
    updateLanguageDisplay(lang);
}

/**
 * Apply translations to all elements with data-i18n attribute
 */
function applyTranslations(lang) {
    const t = translations[lang] || translations.en;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            el.textContent = t[key];
        }
    });

    // Also update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });
}

/**
 * Update language display in dropdown button
 */
function updateLanguageDisplay(lang) {
    const display = document.getElementById('currentLang');
    if (display) {
        display.textContent = langDisplay[lang] || 'EN';
    }
}

/**
 * Initialize i18n on page load
 */
function initI18n() {
    const lang = getCurrentLanguage();
    applyTranslations(lang);
    updateLanguageDisplay(lang);

    // Add click handlers to language options
    document.querySelectorAll('.lang-option').forEach(el => {
        el.addEventListener('click', function (e) {
            e.preventDefault();
            const lang = this.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
}

